'''
输入一个链表，输出该链表中倒数第k个结点。
'''


class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution:
    def FindKthToTail(self, head, k):
        # write code here
        list_nodes = []
        while head:
            list_nodes.append(head)
            head = head.next

        return list_nodes[-k] if 0 < k <= len(list_nodes) else None
